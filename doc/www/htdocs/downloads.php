<h3><A NAME="SECTION_340_10000000000000000">
Bug corrections and Improvements in Version 3.40</A>
</h3>
<P>

<h4><A NAME="SECTION_340_11000000000000000">
General</A>
</h4>
<UL><LI>
 A new set of (pixel-based) quadrature weights has been introduced, besides the older ring-based ones, to improve the accuracy of the Spherical Harmonics calculation. For maps
  containing a signal that is band-limited at <!-- MATH
 $l_{max}=1.5 N_{side}$
 -->
<SPAN CLASS="MATH"><I>l</I><SUB><I>max</I></SUB>=1.5 <I>N</I><SUB><I>side</I></SUB></SPAN>, this allows
  recovery of the <SPAN CLASS="MATH"><I>a</I><SUB><I>lm</I></SUB></SPAN> at almost machine precision. They are supported by map-analysis routines in C++, Fortran, IDL and Python. 
The weights for power-of-2 values of <SPAN CLASS="MATH"><I>N</I><SUB><I>side</I></SUB></SPAN> in <!-- MATH
 $\{16,...,2048\}$
 -->
<SPAN CLASS="MATH">{16,...,2048}</SPAN> are precomputed and shipped in <A NAME="tex2html_340_1"
  HREF="https://sourceforge.net/p/healpix/code/HEAD/tree/trunk/data"><TT>Healpix/data/weight_pixel_n?????.fits</TT></A>, and the missing ones can be computed for any value of <SPAN CLASS="MATH"><I>N</I><SUB><I>side</I></SUB></SPAN> with the <TT>compute_weights</TT> C++ facility.
</UL>
<A HREF=#top>    Back to Top</A><br><br>

<P>

<h4><A NAME="SECTION_340_12000000000000000"></A>
<BR>
<A NAME="tex2html_340_2"
  HREF="./html/index_cxx.htm">C++</A>
</h4>
<UL><LI>
 IMPORTANT: the syntax for specifying ring weights and pixel windows has
  changed! This affects the facilities <TT>anafast_cxx</TT>, <TT>smoothing_cxx</TT>,
  <TT>udgrade_harmonic_cxx</TT>, <TT>alm2map_cxx</TT>, <TT>mult_alm_cxx</TT>.
  Pixel window files have to be specified (with path) using the parameter
  <TT>windowfile</TT>; <TT>ringweights</TT> is used for ring weight files, and <TT>pixelweights</TT>
  for pixel weight files.
<LI>
 Full pixel quadrature weights are now supported in map analysis facilities such as <TT>anafast_cxx</TT>, <TT>smoothing_cxx</TT> and <TT>udgrade_harmonic_cxx</TT>
  using the <TT>pixelweights</TT> parameter.
<LI>
 Experimental <TT>needlet_tool</TT> code for needlet analysis
</UL>
<A HREF=#top>    Back to Top</A><br><br>

